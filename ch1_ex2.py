# Strings
print("Giraffe\nAcademy")

# Adding a quotation mark
print("Giraffe\nAcademy")

# Create a string variable
phrase = "Giraffe Academy"
print(phrase + " is cool")

# Adding a function to strings
phrase = "Giraffe Academy"
print(phrase.lower())

# Convert to upper case
phrase = "Giraffe Academy"
print(phrase.upper())

# To check if the string is upper case
print(phrase.isupper())

# Combining functions on strings
phrase = "Giraffe Academy"
print(phrase.lower().upper().isupper())

# The length of a string
print(len(phrase))

# Extracting characters from a string
print(phrase[0])
print(phrase[1])
print(phrase[3])

# Finding the index of a character from a string
print(phrase.index("G"))
print(phrase.index("a"))
print(phrase.index("Acad"))

# Find out a foreign character
#print(phrase.index("z"))

# Replace certain words inside a string
print(phrase.replace("Giraffe", "Elephant"))











