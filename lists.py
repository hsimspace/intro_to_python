
# Create a list
friends = ["Kevin", "Karen", "Jim", "Oscar", "Toby"]

# Print the whole list
print(friends)

# Accessing a specific element by index
print(friends[0])
print(friends[2])

# Index from the back of the list
print(friends[-1])

# Print more than one elements
# The right index will not print
print(friends[1:3])

# We can modify an element
friends[1] = "Mike"
print(friends)
















